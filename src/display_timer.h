#ifndef DISPLAY_TIMER_H
#define DISPLAY_TIMER_H

#include <Arduino.h>

// ============================================
// Конфигурация задач для мультиплексирования
// ============================================
// Используем FreeRTOS задачи вместо esp_timer для стабильной работы
// независимо от нагрузки WiFi
#define TIMER_BOTTOM_INTERVAL_MS 3  // Интервал задачи нижней платы в миллисекундах (~333 Гц)
#define TIMER_TOP_INTERVAL_MS 3     // Интервал задачи верхней платы в миллисекундах (~333 Гц)

// ============================================
// Инициализация задач для мультиплексирования
// ============================================
// Задачи создаются на ядре 1 (пользовательское) с высоким приоритетом
// Это обеспечивает стабильную работу независимо от WiFi на ядре 0
void bottomBoardTimerInit();  // Инициализация задачи нижней платы
void topBoardTimerInit();     // Инициализация задачи верхней платы

// ============================================
// Запуск задач
// ============================================
void bottomBoardTimerStart();  // Запуск задачи нижней платы
void topBoardTimerStart();     // Запуск задачи верхней платы

// ============================================
// Остановка задач
// ============================================
void bottomBoardTimerStop();   // Остановка задачи нижней платы
void topBoardTimerStop();      // Остановка задачи верхней платы

// ============================================
// Общая инициализация и управление (для обратной совместимости)
// ============================================
void displayTimerInit();       // Инициализация обеих задач
void displayTimerStart();      // Запуск обеих задач
void displayTimerStop();       // Остановка обеих задач

#endif // DISPLAY_TIMER_H
